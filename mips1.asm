deHeart: .word 3816
upDis: .word 0
leftDis: .word 2
rightDis: .word 1
downDis: .word 11


true: .word 1
 
.eqv displayAddress 0x10008000
.eqv black 0xff000000
.eqv red 0xff0000
.text
.globl main
main:
lw $s1,true
bne $s1,1,Exit


lw $s3, upDis
lw $s4, downDis
lw $s5, leftDis
lw $s6, rightDis


beq $s3, 12,loseLife
beq $s4, 12,loseLife
beq $s5, 4, loseLife
beq $s6, 4, loseLife

jal keyboardChanges
lw $s0, defaultCharX
addi $sp, $sp, -4
sw $s0, 0($sp)
jal drawCar 

jal drawBackground

li $v0, 32  
li $a0, 1000 
j main


	
loseLife:
la $a0, rightDis #get address
addi $a1, $zero, 1
sw $a1 0($a0) #save new value

la $a0, leftDis #get address
addi $a1, $zero, 2
sw $a1 0($a0) #save new value

la $a0, downDis #get address
addi $a1, $zero, 11
sw $a1 0($a0) #save new value

la $a0, upDis #get address
addi $a1, $zero, 0
sw $a1 0($a0) #save new value

la $a0, defaultCharX #get address
addi $a1, $zero, 3112
sw $a1 0($a0) #save new value

la $a0, lives #get address
addi $a1, $a0, -1
sw $a1 0($a0) #save new value
jr $ra

Exit:
li $v0, 10 # terminate the program
syscall


drawBackground:
li $t0, displayAddress # $t0 stores the base address for display
la $t1, background
li $t3, 1

whileDrawBackground:
	lw $t2, 0($t1)
	
	sw $t2, 0($t0)
	addi $t0, $t0, 4
	addi $t1, $t1, 4

	addi $t3, $t3, 1
	bne $t3, 1025, whileDrawBackground
	jr $ra



drawHearts:
lw $t2, 0($sp) #stores coordinate
addi $sp, $sp, 4 # adjust stack pointer
li $t4,red
li $t1,displayAddress
lw $t5,lives
addi $t6,$zero,0
loopa:
	beq $t6, $t5, return
	add $t1, $t1, $t2
	sw $t4, ($t1)
	addi $t1,$t1,4
	addi $t6, $t6, 1
	j loopa

return:
jr $ra

drawCar:
lw $t2, 0($sp) #stores coordinate
addi $sp, $sp, 4 # adjust stack pointer
li $t3, black
li $t4, red
li $t1, displayAddress

add $t1, $t1, $t2
sw $t3, ($t1)

addi $t1,$t1,4
sw $t4, ($t1)

addi $t1,$t1,4 
sw $t4, ($t1)

addi $t1,$t1,4 
sw $t3, ($t1)

addi $t1,$t1,256 
sw $t3, ($t1)

addi $t1,$t1,-4 
sw $t4, ($t1)

addi $t1,$t1,-4 
sw $t4, ($t1)

addi $t1,$t1,-4 
sw $t3, ($t1)

addi $t1,$t1,-124 
sw $t4, ($t1)

addi $t1,$t1,4 
sw $t4, ($t1)

jr $ra

keyboardChanges:
li $t9, 0xffff0000 
lw $t8, 0($t9) 
beq $t8, 1, keypress_happened 
jr $ra
keypress_happened:
lw $t3, 4($t9) # this assumes $t9 is set to 0xfff0000
beq $t3, 0x61, res_a # ASCII code of 'a' is 0x61 or 97 in decimal
beq $t3, 0x77, res_w
beq $t3, 0x73, res_s
beq $t3, 0x64 ,res_d
jr $ra

res_a:
lw $s3, leftDis
lw $s4, rightDis
bne $s3,4,DrawA
jr $ra

res_w:
lw $s3, upDis
lw $s4, downDis
bne $s3,12,DrawW
jr $ra

res_s:
lw $s3, upDis
lw $s4, downDis
bne $s4,12,DrawS
jr $ra

res_d:
lw $s3, leftDis
lw $s4, rightDis
bne $s4,4,DrawD

jr $ra

DrawS:
la $a0, defaultCharX #get address
addi $a1, $s0, 256
sw $a1 0($a0) #save new value
addi $a1,$zero,0
addi $a0, $zero, 0

la $a0, downDis #get address
addi $a1, $s4, 1
sw $a1 0($a0) #save new value

addi $a1,$zero,0
addi $a0, $zero, 0

la $a0, upDis #get address
addi $a1, $s3, -1
sw $a1 0($a0) #save new value
  
jr $ra 


DrawW:
la $a0, defaultCharX #get address
addi $a1, $s0, -256
sw $a1 0($a0) #save new value
addi $a1,$zero,0
addi $a0, $zero, 0
la $a0, downDis #get address
addi $a1, $s4, -1
sw $a1 0($a0) #save new value

addi $a1,$zero,0
addi $a0, $zero, 0

la $a0, upDis #get address
addi $a1, $s3, 1
sw $a1 0($a0) #save new value
jr $ra

DrawD:
la $a0, defaultCharX #get address
addi $a1, $s0, 32
sw $a1 0($a0) #save new value
addi $a1,$zero,0
addi $a0, $zero, 0
la $a0, rightDis #get address
addi $a1, $s4, 1
sw $a1 0($a0) #save new value

addi $a1,$zero,0
addi $a0, $zero, 0

la $a0, leftDis #get address
addi $a1, $s3, -1
sw $a1 0($a0) #save new value
jr $ra

DrawA:
la $a0, defaultCharX #get address
addi $a1, $s0, -32
sw $a1 0($a0) #save new value
addi $a1,$zero,0
addi $a0, $zero, 0
